
$(document).ready(function(){

	$("#menu-solution").on("mouseenter", function(){
		$("#solution-dd").show();
	});
	$("#menu-solution").on("mouseleave", function(){
		$("#solution-dd").hide();
	});

	$("#solution-dd").on("mouseenter", function(){
		$("#solution-dd").show();
	});
	$("#solution-dd").on("mouseleave", function(){
		$("#solution-dd").hide();
	});

});